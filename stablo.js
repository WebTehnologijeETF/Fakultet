function bachelor(){
	var x=document.getElementById('bnivo1plus');
	if (x.innerHTML=="+"){
		x.innerHTML="-";
		document.getElementById('bnivo2').style.display="block";
	}
	else {
		x.innerHTML="+";
		document.getElementById('bnivo2').style.display="none";
	}
}

